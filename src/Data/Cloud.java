package Data;

public class Cloud {
	
	private float temp;
	private float water;
	private float xPreference;
	private float yPreference;
	public float getTemp() {
		return temp;
	}
	public void setTemp(float temp) {
		this.temp = temp;
	}
	public float getWater() {
		return water;
	}
	public void setWater(float water) {
		this.water = water;
	}
	public float getxPreference() {
		return xPreference;
	}
	public void setxPreference(float xPreference) {
		this.xPreference = xPreference;
	}
	public float getyPreference() {
		return yPreference;
	}
	public void setyPreference(float yPreference) {
		this.yPreference = yPreference;
	}
}
